# String (text), anything in "" is string.
name = "Yash"

# Integer (whole number) in python "int"
trades = 5

# Float (decimal number) in python "float"
profit = 1234.56

# Boolean (True/False)
is_profitable = True

#Checking a datatype. In python to check a datatype we use "type" keyword.

print(type(name))              # <class 'str'>
print(type(trades))            # <class 'int'>
print(type(profit))            # <class 'float'>
print(type(is_profitable))     # <class 'bool'>
