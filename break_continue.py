for i in range(1, 10):  # for loop for "i" which is each element in the range (1, 10). for loop 
                        # will run for each element in this range one by one.
    if i == 5:          # "if" function inside for loop so we can run the loop til 5 in range (1, 10) 
         break           # and then it will break, means loop will stop when i == 5.
    if i % 2 == 1:       # if function inside for loop so the loop can only run odd number elements 
                        #of the range after the number 5 in the range.
         continue       #loop will continue.
    print(i)            
        

